#include<stdio.h>
int main(void)
{
    printf("\nWhen I do count the clock that tells the time,\n");
    printf("And see the brave day sunk in hideous night;\n");
    printf("When I behold the violet past prime,\n");
    printf("And sable curls all silver'd o'er with white:\n");
    printf("When lofty trees I see barren of leaves,\n");
    printf("Which erst from heat did canopy the herd,\n");
    printf("And summer's green, all girded up in sheaves,\n");
    printf("Born on the bier with white and bristly beard;\n");
    printf("Then of thy beauty do I question make,\n");
    printf("That thou among the wastes of time must go,\n");
    printf("Since sweets and beauties do themselves forsake,\n");
    printf("And die as fast as they see others grow;\n");
    printf("And nothing 'gainst Time's scythe can make defence\n");
    printf("Save breed, to brave him when he takes thee hence.\n");
    return 0;
}  